x = 27  # Nämä luvut voi vaihtaa ja silti laskutoimitukset toimii koska siinä on f
y = 15

yhteenlasku = (x + y)
vahennyslasku = (x - y)
kertolasku = (x * y)
jakolasku = (x / y)

print(f'{x} + {y} = {yhteenlasku}')
print(f'{x} - {y} = {vahennyslasku}')
print(f'{x} * {y} = {kertolasku}')
print(f'{x} / {y} = {jakolasku}')