# Laskuja niin että x ja y numerot voi vaihtaa. Näkyviin tulee koko lasku

x = 27
y = 15

yhteenlasku = x + y
vahennyslasku = x - y
kertolasku = x * y
jakolasku = x / y

print(f'{x} + {y} = {yhteenlasku}')
print(f'{x} - {y} = {vahennyslasku}')
print(f'{x} * {y} = {kertolasku}')
print(f'{x} / {y} = {jakolasku}')